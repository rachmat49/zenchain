package rest

import (
	"github.com/gorilla/mux"

	"github.com/cosmos/cosmos-sdk/client"
	// this line is used by starport scaffolding # 1
)

const (
	MethodGet = "GET"
)

// RegisterRoutes registers auction-related REST handlers to a router
func RegisterRoutes(clientCtx client.Context, r *mux.Router) {
	// this line is used by starport scaffolding # 2
	registerQueryRoutes(clientCtx, r)
	registerTxHandlers(clientCtx, r)

}

func registerQueryRoutes(clientCtx client.Context, r *mux.Router) {
	// this line is used by starport scaffolding # 3
	r.HandleFunc("/auction/orders/{id}", getOrderHandler(clientCtx)).Methods("GET")
	r.HandleFunc("/auction/orders", listOrderHandler(clientCtx)).Methods("GET")

}

func registerTxHandlers(clientCtx client.Context, r *mux.Router) {
	// this line is used by starport scaffolding # 4
	r.HandleFunc("/auction/orders", createOrderHandler(clientCtx)).Methods("POST")
	r.HandleFunc("/auction/orders/{id}", updateOrderHandler(clientCtx)).Methods("POST")
	r.HandleFunc("/auction/orders/{id}", deleteOrderHandler(clientCtx)).Methods("POST")

}
